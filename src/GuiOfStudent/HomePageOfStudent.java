    void fillBoxes(){
        
        try{
          ResultSet rs = db.dbConnect.st.executeQuery("Select * from student_attendence where sid = '"+HomePageOfStudent.Studentid+"'");
            float totalEntries = 0f;
            float totalPresent = 0f;
            
            while(rs.next()){
               
              ResultSetMetaData rsmd = rs.getMetaData();
              int pointer = 4;
                  
              while(pointer <= rsmd.getColumnCount()){


                if(rs.getString(rsmd.getColumnName(pointer)) != null && rs.getString(rsmd.getColumnName(pointer)).equals("Present")){
                    totalPresent++;
                                      totalEntries++;                           

                }
                
                if(rs.getString(rsmd.getColumnName(pointer)) != null && !rs.getString(rsmd.getColumnName(pointer)).equals("Present")){
                  totalEntries++;                           
                }
                
                pointer++;
              }
            }
        
            String str = String.format("%.2f", (totalPresent/totalEntries)*100);
            
        jLabel16.setText(str + " %");
        
        rs.close();
        
        rs = db.dbConnect.st.executeQuery("select * from student_details where sid = '"+HomePageOfStudent.Studentid+"'");
        rs.next();
        jLabel12.setText(String.valueOf(rs.getInt("sfees")));
        jLabel17.setText(String.valueOf(rs.getInt("fine")));
        jLabel14.setText(rs.getString("cname"));
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
            e.printStackTrace();
        }
        
    }
    
