    void fillProfile(){
        
        jTextField8.setText(HomePageOfStudent.Studentid);
        jTextField2.setText(HomePageOfStudent.StudentName);
        
        try{
        ResultSet rs = db.dbConnect.st.executeQuery("select * from student_details where sid = '"+HomePageOfStudent.Studentid+"'");
        rs.next();
        jTextField5.setText(rs.getString("cname"));
        jTextField4.setText(String.valueOf(rs.getInt("sfees")));
        jTextField9.setText(String.valueOf(rs.getInt("fine")));
        jTextField6.setText(String.valueOf(rs.getDate("date")));
        
            byte[] img = rs.getBytes("simage");
            ImageIcon image = new ImageIcon(img);
            Image immg = image.getImage();
            Image myImg = immg.getScaledInstance(jLabel6.getWidth(), jLabel6.getHeight(), java.awt.Image.SCALE_SMOOTH);
            ImageIcon newImage = new ImageIcon(myImg);
            jLabel6.setIcon(newImage);
        
        rs.close();
        
            rs = db.dbConnect.st.executeQuery("Select * from student_attendence where sid = '"+HomePageOfStudent.Studentid+"'");
            float totalEntries = 0f;
            float totalPresent = 0f;
            
            while(rs.next()){
               
              ResultSetMetaData rsmd = rs.getMetaData();
              int pointer = 4;
                         
              while(pointer <= rsmd.getColumnCount()){
                  
                if(rs.getString(rsmd.getColumnName(pointer)) != null && rs.getString(rsmd.getColumnName(pointer)).equals("Present")){
                    totalPresent++;
                    totalEntries++;
                }
                
                if(rs.getString(rsmd.getColumnName(pointer)) != null && !rs.getString(rsmd.getColumnName(pointer)).equals("Present")){
                    totalEntries++;
                }
                pointer++;
              }
            }
             String str = String.format("%.2f", (totalPresent/totalEntries)*100);      
        jTextField7.setText(str + " %");
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    void fillTable(){
      try{  
                javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable1.getModel();
        
        int count = dtm.getRowCount();
        
        while(count-- > 0){
            dtm.removeRow(0);
        }
        
            
            ResultSet rs = db.dbConnect.st.executeQuery("Select * from student_marks where sid = '"+Login.StudentLogin.id+"'order by date desc");
                 int serialNum=0;
            while(rs.next()){
                
                         ResultSetMetaData rsmd = rs.getMetaData();
                         int pointer = 4;
                         
              while(pointer <= rsmd.getColumnCount()){
                      
                  if(rs.getString(rsmd.getColumnName(pointer)) != null){
                ++serialNum;
                Vector v = new Vector();
                v.add(serialNum);
                v.add(rs.getDate("date"));
                
                v.add(rsmd.getColumnName(pointer));
                v.add(rs.getString(rsmd.getColumnName(pointer)));
                
                dtm.addRow(v);
                  }
                  pointer++;
            }
          }
        
      }catch(Exception e){
          JOptionPane.showMessageDialog(null, e);
      }
    }
    
